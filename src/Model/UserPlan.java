package Model;

/**
 *
 * @author Bibek
 */
public class UserPlan {
    private int planId;
    private int userId;
    private String plan;

    public UserPlan() {}

    public UserPlan(int planId, int userId, String plan) {
        this.planId = planId;
        this.userId = userId;
        this.plan = plan;
    }

    public int getPlanId() {
        return planId;
    }

    public void setPlanId(int planId) {
        this.planId = planId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getPlan() {
        return plan;
    }

    public void setPlan(String plan) {
        this.plan = plan;
    }

}
